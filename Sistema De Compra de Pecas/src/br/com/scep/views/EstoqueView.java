    private void jButtonCadastrarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarProdutoActionPerformed

        DefaultTableModel listaDeCadastrados = (DefaultTableModel) jTableProduto.getModel();

        ProdutoService produtoService = new ProdutoService();
        EstoqueService estoqueService = new EstoqueService();
        ProdutoDao prodDao = new ProdutoDao();

        if (!produtoService.camposNaoVazios(jTextFieldNomeProd, jTextFieldQuantProd, jTextFieldPreceProd)) {
            JOptionPane.showMessageDialog(null, "Para adicionar, você precisa preencher todos os dados");
            return;
        }

        try {
            String nomeProd = jTextFieldNomeProd.getText().toUpperCase();
            int quantProd = Integer.parseInt(jTextFieldQuantProd.getText());
            double precoProd = Double.parseDouble(jTextFieldPreceProd.getText());
            Produto prod = new Produto();
            prod.setNomeProd(nomeProd);
            prod.setValor_Produto(precoProd);

            try {
                Produto produtoCadastrado = prodDao.cadastrarProduto(prod, quantProd);
                if (produtoCadastrado != null) {
                    JOptionPane.showMessageDialog(null, "CADASTRADO: " + nomeProd.toUpperCase());
                } else {
                    JOptionPane.showMessageDialog(null, "Não foi possível cadastrar: " + nomeProd.toUpperCase());
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "NÃO FOI POSSÍVEL CADASTRAR: " + nomeProd.toUpperCase());
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Os valores digitados não são válidos.");
        }


    }//GEN-LAST:event_jButtonCadastrarProdutoActionPerformed

    private void jButtonAtualizarProduto1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAtualizarProduto1ActionPerformed

        String nomeProd = jTextFieldNomeProd.getText().toUpperCase();
        int quantProd = Integer.parseInt(jTextFieldQuantProd.getText());
        double precoProd = Double.parseDouble(jTextFieldPreceProd.getText());

        int selectedRow = jTableProduto.getSelectedRow();

        if (selectedRow != -1) {
            DefaultTableModel model = (DefaultTableModel) jTableProduto.getModel();

            if (model.getColumnCount() >= 4) {
                Object idValue = model.getValueAt(selectedRow, 0);
                int idProduto;
                try {
                    if (idValue instanceof Integer) {
                        idProduto = (Integer) idValue;
                    } else if (idValue instanceof Long) {
                        idProduto = ((Long) idValue).intValue();
                    } else if (idValue instanceof String) {
                        idProduto = Integer.parseInt((String) idValue);
                    } else {
                        throw new ClassCastException("Unexpected ID type");
                    }
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(null, "Erro ao converter ID: " + e.getMessage());
                    return;
                }

                String nomeAntigo = (String) model.getValueAt(selectedRow, 1);
                int quantAntiga = (Integer) model.getValueAt(selectedRow, 2);
                double precoAntigo = (Double) model.getValueAt(selectedRow, 3);

                ProdutoService produtoService = new ProdutoService();
                ProdutoDao prodDao = new ProdutoDao();
                Produto prod = produtoService.criarProdutoModel(nomeProd, precoProd, idProduto);

                if (!nomeProd.equals(nomeAntigo) || precoProd != precoAntigo) {
                    prodDao.atualizarProd(prod);

                    model.setValueAt(nomeProd, selectedRow, 1);
                    model.setValueAt(precoProd, selectedRow, 3);
                    JOptionPane.showMessageDialog(null, "PRODUTO ATUALIZADO: " + nomeProd);
                }

                if (quantProd != quantAntiga) {
                    try {
                        prodDao.atualizarQuantEstoque(idProduto, quantProd);
                    } catch (SQLException ex) {
                        System.err.println("Erro:");
                        ex.printStackTrace();
                    }

                    model.setValueAt(quantProd, selectedRow, 2);
                    JOptionPane.showMessageDialog(null, "QUANTIDADE ATUALIZADA: " + nomeProd);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Erro: Número de colunas insuficiente na tabela.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor, selecione um produto da tabela para atualizar.");
        }


    }//GEN-LAST:event_jButtonAtualizarProduto1ActionPerformed

