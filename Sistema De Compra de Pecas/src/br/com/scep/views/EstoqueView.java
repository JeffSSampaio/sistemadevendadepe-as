    private void jButtonApagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonApagarActionPerformed
        int selectedRow = jTableProduto.getSelectedRow();
        ProdutoDao dao = new ProdutoDao();

        int idProduto = (int) jTableProduto.getValueAt(selectedRow, 0);

        if (selectedRow != -1) {
            try {
                dao.deletarProduto((int) idProduto);
                JOptionPane.showMessageDialog(null, "PRODUTO APAGADO [ " + idProduto + "]");
            } catch (SQLException ex) {
                Logger.getLogger(EstoqueView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButtonApagarActionPerformed

