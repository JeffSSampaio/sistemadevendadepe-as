    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        VendasService vendasService = new VendasService();
        List<Vendas> vendaLista = vendasService.consultarRelatorioVendas();
        DefaultTableModel model = (DefaultTableModel) jTableVendas.getModel();
        model.setRowCount(0);
        for (Vendas item : vendaLista) {
            model.addRow(new Object[]{
                item.getId(),
                item.getNomeCliente(),
                item.getNomeProduto(), 
                item.getQuantidade(), 
                item.getValor_total()
            });
        }
    }//GEN-LAST:event_jButton1ActionPerformed

